/**
 * Monster feature with weight indicating its CR impact
 * Weight scale: 0 = minimal, 1 = +0.25 CR, 2 = +0.5 CR, 3 = +1 CR, 4 = +1.5 CR
 */
export interface MonsterFeature {
  name: string;
  weight: number;
  type: 'defensive' | 'offensive' | 'utility' | 'legendary';
  description: string;
}

